// Compound Assignments
// x += y => x = x + y
// x -= y => x = x - y
// x *= y => x = x * y
// x /= y => x = x / y
// x %= y => x = x % y

let x = 5, y = 3;
console.log(x, y);
x += y;
console.log(x, y);
x -= y;
console.log(x, y);
x *= y;
console.log(x, y);
x /= y;
console.log(x, y);
x %= y;
console.log(x, y);

