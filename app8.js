// Logical Operation
// && AND , || OR , ! NOT

let a = true;
let b = true;
console.log("a = ",a);
console.log("b = ",b);
console.log("a && b = ",a && b);
console.log("a || b = ",a || b);
console.log("!a && b = ",!a && b);
console.log("!a || b = ",!a || b);
console.log("a && !b = ",a && !b);
console.log("a ||!b = ",a ||!b);
console.log("!a && !b = ",!a && !b);
console.log("!a ||!b = ",!a ||!b);